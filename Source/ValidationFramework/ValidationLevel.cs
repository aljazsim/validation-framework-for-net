namespace ValidationFramework
{
    /// <summary>
    /// Validation level enumerator.
    /// </summary>
    public enum ValidationLevel
    {
        /// <summary>
        /// No message.
        /// </summary>
        None = 0,

        /// <summary>
        /// Information message.
        /// </summary>
        Info = 1,

        /// <summary>
        /// Warning mesage.
        /// </summary>
        Warning = 2,

        /// <summary>
        /// Error message.
        /// </summary>
        Error = 3
    }
}
